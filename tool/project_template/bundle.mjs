// bundle — packs run.mjs + the whole templates/ tree into ONE self-contained
// file: dist/create-project.mjs. That single file has zero dependency on this
// repo (or any other) — copy it anywhere, or download just that one raw file,
// and run `node create-project.mjs init <dir>`. The templates are embedded as
// base64 so any content survives intact.
//
// Usage: node tool/project_template/bundle.mjs
// Output: tool/project_template/dist/create-project.mjs

import { existsSync, readFileSync, writeFileSync, readdirSync, statSync, mkdirSync } from 'node:fs';
import { join, dirname, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const templatesDir = join(here, 'templates');
const distDir = join(here, 'dist');

function walk(dir, out = []) {
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) walk(p, out);
    else out.push(p);
  }
  return out;
}
function outRelPath(abs) {
  return relative(templatesDir, abs).split(sep).join('/').replace(/\.tmpl$/, '').replaceAll('__dot__', '.');
}

if (!existsSync(templatesDir)) { console.error('bundle: templates/ not found'); process.exit(1); }

const files = {};
for (const f of walk(templatesDir).sort()) {
  files[outRelPath(f)] = readFileSync(f).toString('base64');
}
console.log(`bundle: embedding ${Object.keys(files).length} template file(s)`);

// The emitted runtime. Mirrors run.mjs but writes from the embedded FILES map
// instead of reading templates/ from disk.
const runtime = `// create-project — self-contained Claude-Code-ready project scaffolder.
//
// A SINGLE file with no external dependencies. Generated by bundle.mjs; do not
// edit by hand — edit tool/project_template/templates/** and re-bundle.
//
// Usage (Node 18+):
//   node create-project.mjs init <target-dir> [options]
//   node create-project.mjs list
//   node create-project.mjs --help
//
// Options: --name --slug --repo --stack(generic|flutter|node|python|go)
//          --language --framework --source-dir --test-dir --manifest --force --dry

import { existsSync, writeFileSync, mkdirSync, chmodSync } from 'node:fs';
import { join, resolve, dirname, relative, sep } from 'node:path';

const today = new Date().toISOString().slice(0, 10);

// ── embedded template tree (path -> base64 content) ──────────────────────────
const FILES = ${JSON.stringify(files)};

const PRESETS = {
  generic: { language: '<language>', framework: '<framework / none>', sourceDir: 'src', testDir: 'test', manifest: '<manifest file>',
    verifyDocs: ['node tool/doc_guard/run.mjs check'], verifyCode: ['<lint command>', '<type-check command>', '<test command>'] },
  flutter: { language: 'Dart 3', framework: 'Flutter (Material 3)', sourceDir: 'lib', testDir: 'test', manifest: 'pubspec.yaml',
    verifyDocs: ['node tool/doc_guard/run.mjs check'], verifyCode: ['dart format --output=none --set-exit-if-changed .', 'flutter analyze', 'flutter test'] },
  node: { language: 'TypeScript / Node.js', framework: 'none', sourceDir: 'src', testDir: 'test', manifest: 'package.json',
    verifyDocs: ['node tool/doc_guard/run.mjs check'], verifyCode: ['npm run lint', 'npm run typecheck', 'npm test'] },
  python: { language: 'Python 3', framework: 'none', sourceDir: 'src', testDir: 'tests', manifest: 'pyproject.toml',
    verifyDocs: ['node tool/doc_guard/run.mjs check'], verifyCode: ['ruff check .', 'mypy .', 'pytest -q'] },
  go: { language: 'Go', framework: 'none', sourceDir: 'internal', testDir: 'internal', manifest: 'go.mod',
    verifyDocs: ['node tool/doc_guard/run.mjs check'], verifyCode: ['gofmt -l .', 'go vet ./...', 'go test ./...'] },
};

const argv = process.argv.slice(2);
if (argv.includes('--help') || argv.includes('-h') || argv.length === 0) { printHelp(); process.exit(0); }
const cmd = argv[0];
const flagVal = (n, d) => { const i = argv.indexOf(n); return i >= 0 && argv[i + 1] && !argv[i + 1].startsWith('--') ? argv[i + 1] : d; };
const hasFlag = (n) => argv.includes(n);
const slugify = (s) => String(s).toLowerCase().trim().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
const substitute = (c, t) => c.replace(/\\{\\{([A-Z0-9_]+)\\}\\}/g, (m, k) => Object.prototype.hasOwnProperty.call(t, k) ? t[k] : m);
const decode = (b64) => Buffer.from(b64, 'base64').toString('utf8');

if (cmd === 'list') { for (const p of Object.keys(FILES).sort()) console.log(p); process.exit(0); }
if (cmd !== 'init') { console.error('create-project: unknown command "' + cmd + '". Run with --help.'); process.exit(1); }

const target = argv[1] && !argv[1].startsWith('--') ? resolve(argv[1]) : null;
if (!target) { console.error('create-project: init requires a target directory.'); process.exit(1); }

const stack = flagVal('--stack', 'generic');
const preset = PRESETS[stack];
if (!preset) { console.error('create-project: unknown --stack "' + stack + '". One of: ' + Object.keys(PRESETS).join(', ')); process.exit(1); }

const name = flagVal('--name', target.split(sep).pop());
const slug = flagVal('--slug', slugify(name));
const repo = flagVal('--repo', 'owner/repo');
const language = flagVal('--language', preset.language);
const framework = flagVal('--framework', preset.framework);
const sourceDir = flagVal('--source-dir', preset.sourceDir);
const testDir = flagVal('--test-dir', preset.testDir);
const manifest = flagVal('--manifest', preset.manifest);
const force = hasFlag('--force');
const dry = hasFlag('--dry');

const config = {
  project: { name, slug, repo },
  stack: { preset: stack, language, framework, architecture: 'clean' },
  layers: { sourceDir, testDir, manifest },
  verify: { docs: preset.verifyDocs, code: preset.verifyCode },
};
const tokens = {
  PROJECT_NAME: name, PROJECT_SLUG: slug, REPO: repo, STACK_PRESET: stack,
  STACK_LANGUAGE: language, STACK_FRAMEWORK: framework, SOURCE_DIR: sourceDir,
  TEST_DIR: testDir, MANIFEST_FILE: manifest, DATE: today,
  SCAFFOLD_CONFIG_JSON: JSON.stringify(config, null, 2),
};

console.log('\\nScaffolding "' + name + '" (stack: ' + stack + ') -> ' + target + (dry ? '  [DRY RUN]' : '') + '\\n');
let written = 0, skipped = 0;
for (const rel of Object.keys(FILES).sort()) {
  const dest = join(target, rel);
  const content = substitute(decode(FILES[rel]), tokens);
  if (existsSync(dest) && !force) { console.log('  skip   ' + rel + '  (exists; use --force)'); skipped++; continue; }
  if (dry) { console.log('  would  ' + rel); continue; }
  mkdirSync(dirname(dest), { recursive: true });
  writeFileSync(dest, content);
  if (rel.startsWith('.githooks/')) { try { chmodSync(dest, 0o755); } catch {} }
  console.log('  write  ' + rel);
  written++;
}
if (!dry) {
  console.log('\\nDone. ' + written + ' written, ' + skipped + ' skipped.\\n');
  console.log('Next: fill scaffold.config.json + CLAUDE.md markers; then');
  console.log('  git init && git config core.hooksPath .githooks');
  console.log('  node tool/doc_guard/run.mjs generate');
} else {
  console.log('\\n[dry run] nothing written.\\n');
}

function printHelp() {
  console.log('create-project — single-file Claude-Code-ready scaffolder.\\n\\n' +
    'Usage:\\n  node create-project.mjs init <target-dir> [options]\\n  node create-project.mjs list\\n\\n' +
    'Options: --name --slug --repo --stack(' + Object.keys(PRESETS).join('|') + ')\\n' +
    '         --language --framework --source-dir --test-dir --manifest --force --dry');
}
`;

mkdirSync(distDir, { recursive: true });
const outPath = join(distDir, 'create-project.mjs');
writeFileSync(outPath, runtime);
console.log(`bundle: wrote ${relative(here, outPath)} (${(runtime.length / 1024).toFixed(0)} KB)`);
